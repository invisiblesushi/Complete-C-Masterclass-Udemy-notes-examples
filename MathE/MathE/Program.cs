using System;

namespace MathE
{
    class Program
    {
        static void Main(string[] args)
        {
            //Rund up value
            Console.WriteLine("Ceiling: " + Math.Ceiling(15.3));
            //Round down
            Console.WriteLine("Floor: " + Math.Floor(15.3);

            int num1 = 13;
            int num2 = 9;
            Console.WriteLine("Lower of num1 {0} and num2 {1}", num1, num2, Math.Min(num1, num2));
            Console.WriteLine("Higher of num1 {0} and num2 {1}", num1, num2, Math.Max(num1, num2));




            Console.WriteLine("Hello World!");
        }
    }
}
